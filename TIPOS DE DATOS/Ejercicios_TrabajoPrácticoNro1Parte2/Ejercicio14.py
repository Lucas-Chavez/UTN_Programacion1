# Solicitar al usuario que ingrese los valores de A y B
A = int(input("Ingrese el valor de A: "))
B = int(input("Ingrese el valor de B: "))

# Intercambiar los valores de A y B utilizando una variable auxiliar
auxiliar = A
A = B
B = auxiliar

# Mostrar los valores intercambiados de A y B
print(f"El valor de A después del intercambio es: {A}")
print(f"El valor de B después del intercambio es: {B}")





