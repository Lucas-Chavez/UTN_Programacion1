# Solicitar al usuario que ingrese dos números
numero1 = float(input("Ingrese el primer número: "))
numero2 = float(input("Ingrese el segundo número: "))

# Calcular la diferencia entre los números
diferencia = numero1 - numero2

# Calcular el valor absoluto de la diferencia
distancia = abs(diferencia)

# Mostrar la distancia entre los números
print(f"La distancia entre {numero1} y {numero2} es {distancia}")
